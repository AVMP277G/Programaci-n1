#Variable
número = 10
#Estructura de control If
if numero > 5:
    print("El número es mayor a 5")
print("Fuera de la estructura de control")

#Estructura de control If-Else
if numero > 5:
    print("El número es mayor a 5")
else:
    print("El número es menor o igual a 5")

